<h3>Topic 4: Seating arrangement in a photograph</h3>
<p><em>Question 1</em>: How different is the seating arrangement in actual and in the picture?</p>
<p>Five friends Deepthi, Bhagya, Ramya, Sushil, and Akash request Sugjna, their friend, to take their photograph. Bhagya, who is an attention seeker, is always in the middle of Ramya and Deepti. There is no one to the left of Akash. Ramya always stands next to Sushil. There is no girl between two boys. Who is at the extreme right end in the photograph?</p>
<p><em>Solution</em>:</p>
<p>The seating arrangement in actual would be Deepti- Bhagya-Ramya-Sushil, and Akash. But when the photograph of the above arrangement is taken a right and left references are changed. Since the photograph is held in front of us. The viewer’s right will be the photograph’s left, and the photograph’s right would be the viewer’s left. Hence, Deepti will be at the extreme right end in the photograph.</p>
<h3>Topic 5: Complex Arrangement</h3>
<p>Complex arrangements are very similar to linear arrangement problems in terms of arrangement and properties. But unlike linear arrangement, where the objects had only one property – their positioning, objects in the complex arrangement have multiple properties.</p>
<p>There are two methods to solve the problem:</p>
<ol>
<li><strong>Table Formation:</strong> The data is tabulated to match the properties based on the logical conditions provided in the questions.</li>
<li><strong>Matric Tabulation:</strong> The Properties are matched in the form of a matrix with the help of symbols and special characters to solve the questions.</li>
</ol>
<p>Among both the methods, the number of rows and columns depends on the complexity and the number of properties in the questions.</p>
