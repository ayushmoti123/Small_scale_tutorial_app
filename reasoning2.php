<h3>Topic 3: Circular Arrangement</h3>
<p>The arrangement of physical quantities in a circular manner is called a <strong>Circular arrangement</strong>.</p>
<p>There are two types of circular arrangement:</p>
<ol>
<li><strong>Facing the center:</strong> In this arrangement, every object or a person in the arrangement will be facing the center of the circle.</li>
</ol>
<p>Examples of circular seating arrangement questions include Group discussion and playing cards etc.</p>
<img src="sa2.jpeg">
<p>Schematic representation of type 1 circular seating arrangement</p>
<ol start="2">
<li><strong>Facing outwards:</strong> In this arrangement, every object or a person in the arrangement will be facing outwards.</li>
</ol>
<p>A perfect example of circular seating arrangement questions would be playing musical chairs.</p>
