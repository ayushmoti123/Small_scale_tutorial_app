<h2>What is a Seating Arrangement?</h2>
<p>The seating arrangement is the logical arrangement of either objects or people in a logical manner. One has to either perform the arrangement to answer the questions or decode the predefined arrangement by applying the logical analysis.</p>
<p>Listed below are the detailed explanation of each topic covered in Seating arrangement questions:</p>
<h3>Topic 1: Assumptions</h3>
<ol>
<li>Unless specified the direction at the top page is always assumed as North.</li>
<li>The right side of the page is the viewer’s left side and vice versa.</li>
</ol>
<h3>Topic 2: Linear Arrangement</h3>
<p>Any logical arrangement of objects or people either horizontally or vertically or diagonally is known as<strong>Linear arrangement</strong><p>
<p>Ex: The row of the travellers in a train, students in a prayer hall, etc.</p>
