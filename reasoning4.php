<h2><span style="font-weight: 400;">Reasoning Analogy &#8211; Concept &amp; Basics</span></h2>
<p>Before solving the questions, an aspirant must understand the concept of analogy. An analogy is a word used in terms of comparing two things or finding a relation between two things.</p>
<p>Similarly, in questions related to Analogy in reasoning, options are given, which may or may not be associated with each other and candidates need to find the odd option out.</p>
<p><span style="font-weight: 400;">The analogy questions may be asked in various forms. Given below are the type of questions asked for analogies:</span></p>
<ul>
<li><b>Numerical Analogy (Odd One Out) &#8211;<span style="font-weight: 400;"> A set of options may be given based on a certain pattern, and one of them may be unlikely to follow the pattern and candidates need to choose the odd one out</span></b></li>
<li><b>Numerical Analogy (Choose a similar pair) &#8211; <span style="font-weight: 400;">An analogy may be given in the question and candidates may have to find a similar analogy, based on the same pattern from the given options</span></b></li>
<li><strong>Alphabetical/Word Analogy (Odd One Out) &#8211;</strong> <span style="font-weight: 400;">Options for different word analogy may be given, in which one may not be following the set pattern, candidates need to find that odd one out from the given options</span></li>
<li><strong>Alphabetical/Word Analogy (Choose a similar pair) &#8211;</strong> <span style="font-weight: 400;">An analogy may be given based on a set pattern and candidates may have to find the word analogy pair from the given options, which follows the exact same pattern</span></li>
</ul>
<h3><span style="font-weight: 400;">Tips &amp; Tricks to Solve Reasoning Analogy</span></h3>
<p><span data-preserver-spaces="true">Given below are a few tips which may help you prepare for the reasoning analogy questions and also help you solve the questions faster:</span></p>
<ul>
<li><span data-preserver-spaces="true">The first thing to solve an analogy question is to judge the pattern that is being followed in the given analogy.</span></li>
<li><span data-preserver-spaces="true">If a candidate is unable to analyse the pattern followed, he/she must refer to the options given. It shall simply analysing the pattern.</span></li>
<li><span data-preserver-spaces="true"> For numerical questions, squares, cubes, multiplication, addition, etc. based patterns may be followed. So ensure that you recheck these pattern as well.</span></li>
<li><span data-preserver-spaces="true">It is easy to score marks in questions based on analogy as they are direct and not very complicated.</span></li>
<li><span data-preserver-spaces="true"> Having knowledge of basic static GK shall also be important for these questions.</span></li>
</ul>
<h3><span style="font-weight: 400;">Solved Examples &#8211; Reasoning Analogy</span></h3>
<p><span style="font-weight: 400;">Given below are a few sample questions on reasoning analogy based on the latest pattern and considering the type of questions which may be asked.</span></p>
<p><b>Q 1.</b><span style="font-weight: 400;"> From the given options, find the pair which is similar to the given pair: 8:4</span></p>
<ol>
<li style="font-weight: 400;"><span style="font-weight: 400;">27:9</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">216:32</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">72:24</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">45:5</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">37:13</span></li>
</ol>
<p><b>Answer: (1) 27:9</b></p>
<p><b>Solution: </b></p>
<p><span style="font-weight: 400;">The pattern followed is “Cube of a number: square of the same number”</span></p>
<p><span style="font-weight: 400;">So, (2×2×2) : (2×2) </span></p>
<p><span style="font-weight: 400;">= 8:4</span></p>
<p><span style="font-weight: 400;">Similarly, (3×3×3) : (3×3)</span></p>
<p><span style="font-weight: 400;">= 27:9</span></p>
