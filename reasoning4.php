<p><span style="font-weight: 400;">24×3 = 72 and the given option is 24:70</span></p>
<p><span style="font-weight: 400;">33×3 = 99 which gives 33:99</span></p>
<p><span style="font-weight: 400;">121×3 = 363 which gives 121:363</span></p>
<p><span style="font-weight: 400;">1×3 = 3 which gives 1:3</span></p>

