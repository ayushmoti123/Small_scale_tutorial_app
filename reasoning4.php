<p><b>Q 5.</b><span style="font-weight: 400;"> Find the odd one out from the given pairs.</span></p>
<ol>
<li style="font-weight: 400;"><span style="font-weight: 400;">5:15</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">24:70</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">33:99</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">121:363</span></li>
