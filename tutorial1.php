</ul>
<p><strong>Tier-II Exam</strong></p>
<ul>
<li><strong>Paper-I (Quantitative Abilities)</strong>: This paper will evaluate a candidate’s calculation and numeric skills. The relevant topics that should be covered are whole numbers, decimals, fractions and relationships between numbers, Percentage, Ratio &amp; Proportion, Square roots, Averages, Interest, Profit and Loss, Discount, distance, surds, Graphs of Linear Equations, Complementary angles, Heights and Distances, Histogram, Frequency polygon, Bar diagram &amp; Pie chart, etc.</li>
<li><strong>Paper-II (English Language and Comprehension)</strong>: Paper-II of SSC CGL English preparation is designed to evaluate a candidate’s grammar and language skills. The important topics of SSC CGL English preparation in Paper II are &#8211; spotting the error, fill in the blanks, synonyms and antonyms, spelling/ detecting misspelt words, idioms &amp; phrases, one word substitution, active/ passive voice of verbs, conversion into direct/ indirect narration, shuffling of sentence parts, shuffling of sentences in a passage, etc.</li>
</ul>
