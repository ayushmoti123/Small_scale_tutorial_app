<ul>
<li><strong>Paper-III (Statistics): </strong>This paper will have topics related to Collection and Representation of data, Skewness &amp; kurtosis, Measures of Central Tendency, correlation &amp; regression, Probability Theory, time series analysis, Index numbers, etc.</li>
<li><strong>Paper-IV (General Studies-Finance and Economics):</strong></li>
<li><strong>Part – A- (Finance &amp; Accounts): </strong>This paper will be for 80 marks, and it includes topics related to Rectification of Errors, Manufacturing, Trading, Profit &amp; Loss Appropriation Accounts, Balance Sheet Distinction between Capital and Revenue Expenditure, Depreciation Accounting, Valuation of Inventories, Non-profit Organizations Accounts, Receipts and Payments and Income &amp; Expenditure Accounts, Bills of Exchange, Self-Balancing Ledgers, etc.</li>
<li><strong>Part -B- (Economics and Governance): </strong>This paper will be for the remaining 120 marks and it includes topic related to Money &amp; Banking, Economic reforms, Theory of demand and supply, Economics- Micro &amp; macro, Market supply analysis, etc.</li>
</ul>
<p><strong>Tier III Exam</strong></p>
<p>This exam will be a subjective paper and can be attempted in Hindi/English. Candidates need to prepare for applications, letters, essays, etc.</p>
<p><strong>Tier IV Exam</strong></p>
<p>This exam will be done to evaluate a candidate’s typing and basic computer-related skills.</p>
<p>The “Data Entry Speed Test” Skill Test will be conducted for a passage of about 2000 (two thousand) key depressions for a duration of 15 (fifteen) minutes.</p>
<p>The Computer Proficiency Test (CPT), will have Word Processing, Spreadsheet and Generation of Slides as the test topics.</p>
