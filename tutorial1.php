<ul>
<li><strong>English Comprehension</strong>: This section is prepared to test the candidates writing and comprehension abilities. SSC CGL English preparation will cover essays, grammar, passages, etc.</li>
</ul>
<p>Given below are a few relevant topics of English language that are sure to be asked in the examination:</p>
<ul>
<li>List Of Synonyms and Antonyms</li>
