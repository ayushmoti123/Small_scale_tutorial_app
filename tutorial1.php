<li>List of Arjuna Award Winners</li>
<li>FIFA World Cup Winners List</li>
<li>Fathers Of Various Fields</li>
</ul>
<ul>
<li><strong>Quantitative Aptitude: </strong>This section is designed to test the number sense and calculation skills of the candidates. The important topics in this section are whole numbers, decimals, fractions and relationships between numbers, Percentage. Ratio &amp; Proportion, Square roots, Averages, Interest, Profit and Loss, Discount, Partnership Business, Mixture and Alligation, Time and distance, Time &amp; Work, Basic algebraic identities, geometrical problems, etc.</li>
