<li>Reasoning Analogies</li>
<li>Order &amp; Ranking</li>
</ul>
<ul>
<li><strong>General Awareness: </strong>This section is designed to test the general knowledge of candidates. It will include all important topics related to History, Culture, Geography, Economic Scene, General Policy &amp; Scientific Research. 
<ul>
<li>Daily Current Affairs</li>
<li>Daily News Analysis</li>
</ul>
</li>
</ul>
<p>For the preparation of the General Awareness section, important topics that candidates must go through are listed below:</p>
<ul>
