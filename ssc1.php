<p><h2>Frequently Asked Question – SSC Exam 2023</h2>
        <h3 >Q.1. What is the highest post in SSC Exam?</h3>
        <h3>Ans. Assistant Accounts Officer OR Assistant Audit Officer is the highest post that a candidate can get through by qualifying Combined Graduate Level SSC Examination. AAO is a Group “B” Gazetted (Non-Ministerial) post with the grade pay 4800 rupees.</h3>
        <h3>Q.2. What is the minimum age criteria for all SSC exams?</h3>
        <h3>Ans. SSC has set different age limit criteria for different SSC exams. The minimum age of a candidate applying for SSC JE, SSC CHSL, SSC CGL, and SSC GD must be 18 years and that of a candidate applying for SSC CPO must be 20 years of age. The upper age limit for various SSC exams differs as per the category of the candidates.</h3>
        <h3>Q.3. What are the subjects covered in various SSC exam?</h3>
        <h3>Ans. The common subjects of various SSC exams are Reasoning Ability, General Awareness, Quantitative Aptitude, and English. However, there are exams in Staff Selection Commission that require candidates to have professional knowledge as well. A detailed list of subjects along with the topics covered is given in the syllabus page of respective exams.</h3>
        <h3>Q.4. What is the negative marking scheme in the SSC exam?</h3>
        <h3>Ans. There is no negative marking in SSC GD, While, for every wrong attempt candidates will be penalised with 0.25 marks in SSC CGL, SSC CPO and SSC JE exam. In SSC CHSL candidates will lose 0.5 marks for each wrong answer.</h3>
        <h3>Q.5. How do the Staff Selection Commission intimates candidates about the SSC exam recruitments?</h3>
        <h3>Ans. Candidates will find all the information related to various SSC exams on the SSC Official Website i.e. (www.ssc.nic.in). Candidates will also get all the latest information regarding all SSC examinations on this page itself.</h3>
        <h3>Q 6. What is the minimum education qualification for all SSC exams?</h3>
        <h3>Ans. SSC Conducts various exams annually and all have different requirements for the educational qualification of the candidates. The SSC Exam academic qualification for the various post is as follows:</h3>
        <ul><li>SSC CGL – A Graduation Degree in any discipline with a minimum of 50% marks.</li>
            <li>SSC CPO – A Graduation Degree in any discipline from a recognized university (no minimum percentage specified as such).</li>
            <li>SSC CHSL – A 12th pass certificate from any board or university</li>
            <li>SSC JE – A degree or Diploma in Engineering relevant to the post applied for.</li>
            <li>SSC GD – A 10th Pass certificate from a recognized board.</li>
        </ul>
        <h3>Q.5. How should candidates prepare for Staff election Commission Exams?</h3>
        <h3>Ans. Candidates will find all the relevant study material for SSC exams both online and offline or they can also download the Staff Selection Commission App for assistance.</h3>
        
