<h3>Steps to solve the questions:</h3>
<ol>
<li>Don not read the complete banking question paper in one go, read sentence by sentence and construct the logic accordingly.</li>
<li>Order or Position will be either horizontal or vertical. Decide the direction of the arrangement.</li>
<li>Consider all possible cases, but discard all except the correct one at the end to get the correct order or ranking.</li>
<li>Non-consideration of all the possible cases will lead to the wrong order or ranking.</li>
<li>Either use the ranking to get the total number of objects/people or use the total number to evaluate the ranking.</li>
<li>Follow the processor arrangement in aright way; reverse engineering technique doesn&#8217;t work all the time to get the correct answer.</li>
</ol>
<h3>Seven rules to remember to solve ranking and order in the banking question paper.</h3>
<p><strong>Rule #1.</strong></p>
<p>The total number of a person/objects in a group or class is equal to one less than the sum of the positions of the same person from both the ends (either right and left or top and bottom). Since the same person is counted twice in the sum, the final answer is one less than the total sum.</p>
<img src="a2.png">
<li>Total number of objects/persons</li>

<p>= [(sum of positions of the same person/object from both sides) – 1]
<p><strong>Example 1:</strong></p>
<p>In a row of persons, the position of Saket from the left side of the row is 27th and position of Saket from the right side of the row is 34th. Find the total number of students in the row?</p>
<ol>
<li>60</li>
<li>61</li>
<li>62</li>
<li>59</li>
</ol>
<p><strong>Solution:</strong></p>
<p>Total number of students</p>
<p>= (Position of Saket from left + Position of Saket from right) -1</p>
<p>Total number of students = (27 + 34) – 1 = 61 – 1 = 60.</p>
<p>Hence the correct answer is option A.</p>
<p><strong>Rule#2</strong></p>
<p>The total number of person/object in a group is the sum of before or after the given person in a row and the position of the same person from the other side.</p>
<p>Total no. of persons/objects = No. of persons/objects before or after the given person in a row + Position of the same person from the other side.</p>
<p><img src="a3.png"></p>
<p>An orderly arrangement of pencils in a linear way.</p>
<p><strong>Example 2:</strong></p>
<p>In a row of persons, the position of Aparna Nair from the left side of the row is 27th and there are 5 persons after her in the row. Find the total no. of persons in the row?</p>
<p>Solution:</p>
<p>No. of persons in the row = Position of Aparna from left + No. of persons after Aparna</p>
<p>⇒ Total no. of persons = 27 + 5 = 32</p>

<p><strong>Rule#3</strong></p>
<p>If the positions of two objects/persons are given from the opposite ends and also the total number of persons/objects, then the problem can be addressed in two different ways to determine the number of persons between these two persons/objects.</p>
<p><strong>Case 1:</strong></p>
<p>Overlapping:</p>
<p>The total number of objects or persons in a group is always lesser than the addition of the position of two objects or persons from ends.</p>
<p><strong>Example 3:</strong></p>
<p>The number of objects between two different persons = Total number of books – (Sum of positions of two different persons from opposite sides)</p>
<p>There are 24 students in dance class, and the teacher is planning for an arrangement of students on stage. Sampratha is 9th from the left side of the row and Supreetha is 22nd from the right side of the row. Find the number of dancers standing between the sisters Sampratha and Supreetha?</p>
<p>A.4</p>
<p>B.5</p>
<p>C.6</p>
<p>D.7</p>
<p><strong>Solution:</strong></p>
<p>Adding the position of Sampratha and Supreetha we get:</p>
<p>= 9 + 22 = 31</p>
<p>The result ‘31’ is greater than the total number of students in a dance class.</p>
<p>Therefore the number of dancers standing between the sisters will be = [(Position of Sampratha from left + Position of Supreetha from right) – Total number of dancers – 2]
<p>The number of dancers between Sampratha and Supreetha</p>
<p>= (9+22) – 24 – 2 = 31 – 24 – 2 = 5.</p>
<p>Hence the correct answer is option B.</p>
<p><strong>Case 2:</strong></p>
<p><strong>Non &#8211; overlapping:</strong></p>
<p>The total number of objects or persons in a group is always greater than the addition of the position of two objects or persons from ends.</p>
<p><strong>Example 4:</strong></p>
<p>There are 64 history books arranged in a row at central library Bangalore. Ancient history is 25th from the left side of the row and Middivel history is 30th from the right side of the row. What is the total number of books between Ancient and Middivel history?</p>
<p>A.6</p>
<p>B.7</p>
<p>C.8</p>
<p>D.9</p>
<p><strong>Solution:</strong></p>
<p>Adding the position of ancient and midlevel history books, we get:</p>
<p>Ancient histroy+ Middivel history = 25 + 30 = 55</p>
<p>Hence the number ‘55’ should be less than the total number of books.</p>
<p>∴ The number of books between ancient and midlevel history = Total number of books – (Place value of Ancient history book from left + Place value of Middivel history from right)</p>
<p>The number of books between ancient and midlevel history = 64 – (25+30) = 64 – 55 = 9</p>
<p>Hence the correct is option D.</p>
<p><strong>Rule #4</strong></p>
<p><strong>Non-predictable order/ranking.</strong></p>
<p>If the data in the question provides only then information of position different objects or persons then it is impossible to find the total number of objects or people in a group or class. As the cases can either be an overlapping or non-overlapping one. In such a situation, the final answer will always be found. Save the time by not trying to solve these type of questions.</p>
<p><strong>Example 5:</strong></p>
<p>Deepavali or Diwali a festival lights in India. One can find the row of lamps in every house these days. Chaitra lights a row of the lamp in her home. A square-shaped lamp is at 18th from left, and a circular-shaped lamp is at 25th position in a row from right. Find the total number of lamp Chaitra had lit?</p>
<p>A.27</p>
<p>B.30</p>
<p>C.43</p>
<p>D. Cannot be determined.</p>
<p><strong>Solution:</strong></p>
<p>The scenario can be either be of Overlapping or non-overlapping one. Hence the correct answer is option D.</p>
<p><strong>Rule#5</strong></p>
<p>Swapping of position to find the order/ ranking</p>
<p>In this section, the placement or the position of the two objects/persons are interchanged. The position of the two people or objects is examined before and after the interchanged.</p>
<p>The place value or the position of the second person from the same side as before interchanging</p>
<p>= Position of 2nd person from the same side before interchanging + (Position of 1st person after interchanging – position of 1st person before interchanging from the same side)</p>
<p><strong><span data-preserver-spaces="true">Example 6:</span></strong></p>
<p><span data-preserver-spaces="true">Soldiers Punita and Mitali and are standing in a row of female soldiers. Punita is 18th from the left side of the row, and Mitali is 24th from the right side of the row. If they interchange their positions, Punita becomes 31st from left. Find:</span></p>
<ol>
<li><span data-preserver-spaces="true">The new position of Mitali from the right side</span></li>
<li><span data-preserver-spaces="true">The total number of female soldiers in a row.</span></li>
<li><span data-preserver-spaces="true">Number of soldiers standing between Punita &amp; Mitali</span></li>
</ol>
<p><strong>Solution:1</strong></p>
<p>The new position of Mithali from right side = Position of Mithali from the right side before interchanging + (Position of Punita from the left side after interchanging – Position of Punita from the left side before interchanging)</p>
<p>New position of Mithali from right side = 24 + (31 – 18) = 24 + 13 = 37</p>
<p>The new position of Mithali is 37th.</p>
<p><strong>Solution 2:</strong></p>
<p>The total number of person between A and B can be found in two different ways.</p>
<ol>
<li>Total no. of persons = (A’s position from right before interchanging + A’s position from left before interchanging) – 1</li>
</ol>
<p>or</p>
<ol start="2">
<li>Total no. of persons = (B’s position from right after interchanging + A’s position from left before interchanging) – 1</li>
</ol>
<p>Since we don&#8217;t know the position of Punita from right before interchanging. We can&#8217;t use the first method. We can use the second method as we know both the values.</p>
<p>The Total number of female soldiers = (Mithali’s position from right before interchanging + Punita’s position from left before interchanging) – 1</p>
<p>= 37+18 -1 = 54.</p>
<p><strong>Solution 3:</strong></p>
<p>To find the total number of people between any two persons.</p>
<p>No. of persons between A &amp; B = (Position of A from left after interchanging– Position of A from left before interchanging) – 1</p>
<p>The total numbers of soldiers between Punita &amp; Mithali = (Position of Punita from left after interchanging– Position of Punita from left before interchanging) – 1</p>
<p>= (31 – 18) – 1 = 13 – 1 = 12</p>
<p><strong>Rule#6</strong></p>
<p>If positions of two objects from opposite sides of the row are known there is a third object right in the middle of the two, then the total number of objects can be evaluated based on the position of the third object.</p>
<p><strong>Case 1:</strong></p>
<p>The position of the third object is known from both the sides</p>
<p><strong>Case 2:</strong></p>
<p>The position of the third object is known from either of the sides.</p>
<p><strong>Example 7:</strong></p>
<p>There is a pride of lions and its cubs in a row, the position of eldest lioness from the left side of the row is 9th &amp; position of youngest lioness from the right side of the row is 8th. If the newborn cub is sitting just in the middle of eldest &amp; youngest and position of cub from the left side of the row is 15th. Find the total number of lions the row?</p>
<p><strong>Solution:</strong></p>
<p>Position of a cub from left is 15th, and the eldest lioness from left is 9th so there are 15 – 9 – 1 = 5 lions are sitting between eldest and youngest lioness. As the cub is sitting in the middle of the eldest and youngest lioness so there must also be 5 persons sitting between the youngest lioness and a cub.</p>
<p>Thus position of a cub from right =</p>
<p>Position of youngest from right + 5 + 1 =</p>
<p>= 8 + 6 = 14</p>
<p>Total number of lions = (Sum of positions of cubs from both sides – 1)</p>
<p>= (15 + 14) – 1 = 29 – 1 = 28</p>
<p><strong>Rule#7</strong></p>
<p>To find the minimum number of members in the group.</p>
<p>The Minimum number of persons = Sum of positions of persons from both sides – Persons between them – 2.</p>
<p><strong>Example 8:</strong></p>
<p>If the position of A from the left side of a row is 15th and position of B from the right side of a row is 19th and only 1 person is sitting in the middle of A &amp; B. Find the minimum number of persons that can be seated in this row?</p>
<p><strong>Solution:</strong></p>
<p>The total number of persons = 15 + 19 – 1 – 2 = 31.</p>
