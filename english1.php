<h1>Synonyms and Antonyms</h1>
<p><span style="font-weight: 400;">Candidates tend to lose marks in the English section if their vocabulary is not up to the mark. The usage of Synonyms and Antonyms is essential in day to day communication as well. Hence, we are providing the list of Synonyms and Antonyms containing more than 400 English synonym and antonym words. </span></p>
<h3>Synonyms And Antonyms &#8211; Meanings</h3>
<p><span style="font-weight: 400;">Before learning various words of synonyms and antonyms, let us first understand the meaning of both the words.</span></p>
<h3><span style="font-weight: 400;">What is a Synonym?</span></h3>
<p><span style="font-weight: 400;">A synonym is a word/phrase, the meaning of which is the same or nearly the same as another word or phrase. Words that are synonyms are described as synonymous.</span></p>
<p><b>Synonym examples:</b></p>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">Artful &#8211; Crafty</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Ballot &#8211; Poll</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Chorus &#8211; Refrain</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Deceptive &#8211; Misleading</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Enormous &#8211; Immense</span></li>
</ul>
<h3><span style="font-weight: 400;">What is an Antonym?</span></h3>
<p><span style="font-weight: 400;">An antonym is a word/phrase that means the opposite of another word or phrase. Check the examples.</span></p>
<p><b>Antonym examples:</b></p>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">Admire &#8211; Detest</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Bravery &#8211; Cowardice</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Crooked &#8211; Straight</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Dainty &#8211; Clumsy</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Economise &#8211; Waste</span></li>
</ul>
